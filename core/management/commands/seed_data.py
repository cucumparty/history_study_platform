    help = "Заполнить базу данных тестовыми историческими данными"

    def handle(self, *args, **options):
        self.stdout.write("Создаём эпохи...")
        self._create_eras()
        self.stdout.write("Создаём лекции...")
        self._create_lectures()
        self.stdout.write("Создаём факты...")
        self._create_facts()
        self.stdout.write("Создаём таймлайн...")
        self._create_timeline()
        self.stdout.write(self.style.SUCCESS("Готово! База данных заполнена."))

